#include "sh42.h"

int			only_spaces_and_sep(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] != ' ' && str[i] != '\t' && str[i] != '\n' && str[i] != '\v'
		&& str[i] != '&' && str[i] != '|' && str[i] != ';' && str[i] != '\r')
			return (0);
		i++;
	}
	return (1);
}
